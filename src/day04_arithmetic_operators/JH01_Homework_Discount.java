package day04_arithmetic_operators;

public class JH01_Homework_Discount {
    public static void main(String[] args) {
        int regularPrice=100;
        float discoutAmount=regularPrice*20.0F/100;
        float salesPrice=regularPrice-discoutAmount;

        System.out.println("Regular Price: $"+regularPrice);
        System.out.println("Discount Amount: $"+discoutAmount);
        System.out.println("Sale Price: $"+salesPrice);






    }
}
