package day13_branching_statements;

public class J06_MultiplyWithAddition {
    public static void main(String[] args) {

        int num1=5;
        int num2=7;
        int sonuc=0;

        for (int i = 1; i <= num1; i++) {
            sonuc=sonuc+num2;

        }
        System.out.println(sonuc);

    }
}
