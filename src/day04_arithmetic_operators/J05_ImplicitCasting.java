package day04_arithmetic_operators;

public class J05_ImplicitCasting {
    public static void main(String[] args) {

        /* Implicit Casting
        küçük veri tipini büyük veri tipinin
        içine koyma işlemi
         */

        byte b = 25;
        int i = b;
        System.out.println(i);

        int i1=10;
        System.out.println(i1);

        double d=i1;
        System.out.println(d);








    }
}
