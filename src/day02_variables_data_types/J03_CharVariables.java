package day02_variables_data_types;

public class J03_CharVariables {
    public static void main(String[] args) {

        char a='a';
        char questionMark='?';


        System.out.println(a);
        System.out.println(questionMark);
        char single='\'';
        System.out.println(single);

        //Decimal Code
        char asciiDecimal=65;
        System.out.println(asciiDecimal);

        //Hexadecimal Code
        char asciiHexadecimal=0x28;
        System.out.println(asciiHexadecimal);

        char asciiUniCode='\u6541';
        System.out.println(asciiUniCode);



    }
}
