package day05_assignment_unary;

public class J02_ShorthandOperators {
    public static void main(String[] args) {

        int x=5;


        x+=6; //x=x+6;
        System.out.println(x);

        int y=12;

        y-=4; //y=y-4;
        System.out.println(y);

        int z=12;
        z*=5; //z=z*5;
        System.out.println(z);

        int t=20;
        t/=4; //t=t/4;
        System.out.println(t);







    }



}
