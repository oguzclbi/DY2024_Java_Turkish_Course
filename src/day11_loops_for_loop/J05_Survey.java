package day11_loops_for_loop;

public class J05_Survey {
    public static void main(String[] args) {
        for (int i = 1; i <= 20; i++) {
            System.out.println(i+". kişi");
            System.out.println("--------");
            System.out.println("Adınız?");
            System.out.println("Soyadınız?");
            System.out.println("Mesleğiniz");
            System.out.println("Yaş?");
            System.out.println("Hangi Takımlısınız?");
            System.out.println("===================");

        }

    }
}
