package day04_arithmetic_operators;

public class J00_ArithmeticOperators {
    public static void main(String[] args) {

        System.out.println(10 + 5); //15
        System.out.println(10 - 5); //5
        System.out.println(10 / 5); //2
        System.out.println(10 * 5); //50
        System.out.println(10 % 3); //1
        System.out.println(9 % 2); //1
        System.out.println(14 % 2); //0
        System.out.println(1234%10); //4 (Birler basamağındaki sayıyı bulur)
        System.out.println(1234%100); //34 (Yüzler basamağındaki sayıyı bulur)
        System.out.println(1223%7); //5

    }
}
